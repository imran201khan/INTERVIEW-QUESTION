SELECT COUNT(R.R_ID) AS "GENDER_COUNT",P.P_SUBJECT,M.M_GENDER FROM RESEARCHER R 
INNER JOIN RESEARCH_PAPER RP ON R.R_ID=RP.R_ID INNER JOIN RESEARCH_MENTOR RM ON R.R_ID=RM.R_ID 
INNER JOIN PAPER P ON P.P_ID=RP.P_ID INNER JOIN MENTORS M ON M.M_ID=RM.M_ID
WHERE   P.P_SUBJECT LIKE "%b%" 
